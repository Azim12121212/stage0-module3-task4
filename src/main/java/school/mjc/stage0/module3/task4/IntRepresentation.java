package school.mjc.stage0.module3.task4;

public class IntRepresentation {
    public static void main(String[] args) {
        char c, h, a, r;
        c = 'c';
        h = 'h';
        a = 'a';
        r = 'r';

        int intC = c;
        int intH = h;
        int intA = a;
        int intR = r;

        System.out.println(intC);
        System.out.println(intH);
        System.out.println(intA);
        System.out.println(intR);
    }
}
